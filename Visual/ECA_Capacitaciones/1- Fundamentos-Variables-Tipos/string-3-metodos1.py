# Capitalize()
saludos = 'hola como estas!?'

print(saludos.capitalize())
print(saludos)

saludos = saludos.capitalize()

print(saludos)

# Lower()

palabra = 'HOLA Y CHAU'
print(palabra)

palabra = palabra.lower()

print(palabra)

# Upper()

texto = 'estoy bien...'
print(texto)

texto = texto.upper()
print(texto)

