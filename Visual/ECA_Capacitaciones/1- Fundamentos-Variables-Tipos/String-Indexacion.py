# Indexacion

lenguaje = 'python'

print(lenguaje)

print(lenguaje[0])

print(lenguaje[2])

print(lenguaje[0:3]) # lenguaje[0], lenguaje[1], lenguaje[2]

print(lenguaje[2:5]) # lenguaje[2], lenguaje[3], lenguaje[4]

print(lenguaje[:3]) # Es equivalente a lenguaje[0:3]

print(lenguaje[3:])

print(lenguaje[3:6]) # Es equivalente a escribir, lenguaje[3], lenguaje[4] , lenguaje[5]

