# Indexacion
# el primer caracter es 0 a x. contraro si va de atras a adelante que empienza con -1.


lenguaje = 'SANTINO'

print(lenguaje)

print(lenguaje[0])

print(lenguaje[3])

print(lenguaje[0:3]) #lenguaje[0],lenguaje[1],lenguaje[2]
# no se incluye la posicion 3

print(lenguaje[2:8]) #lenguaje[2],lenguaje[3],lenguaje[4],lenguaje[5],lenguaje[6],lenguaje[7]
print(lenguaje[:8])
print(lenguaje[:-1])
