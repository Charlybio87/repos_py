#---Comentario de Linea---
#---NUMEROS LITERALES---
print("2")  # se imprimira un caracter de tipo numerico (dos).
#---NUMERROS ENTEROS
print(2)    # se imprimira un valor numerico entero (dos).
#---NUMEROS OCTALES Y HEXADECIMALES---
print(0o123)    # 0o... es un nuemro octal. Se imrime su representacion en decimal.
print(0x123)    # 0x... es un numero hexadecimal. 
#---NUMEROS FLOTANTES---
print(2.5)
print(-0.4)
#--- NUMERO CON POTENCIA
print(3e8)
#--- CODIFICANDO FLOTANTE
print(6.62607E-34)
print(0.0000000000000000000001)
#--- CADENAS---
print("Me gusta \"Monty Python\"")  # \"...\" impreme las comillas con caracter/es.
print('Me gusta "Monty Python"')    # apostrofes '"..."' imprime comillas con caracter/es.

print("I'm Monty Python.")  #dos mas formas de representacion 
print("I\'m Monty Python.")
print('I\'m Monty Python.')
#--- BOOLEANOS---
print(True > False)
